static uint32_t cols() { return fb.width / FONT_WIDTH; }
static uint32_t rows() { return fb.height / FONT_HEIGHT; }

// ===================== 对外接口实现 =====================

void init_fb(boot_info *bi) {
  if (bi->fb_addr == 0) {
    return;
  }

  uint64_t fb_phys = bi->fb_addr;
  uint32_t fb_pitch = bi->fb_pitch;
  uint32_t fb_w = bi->fb_width;
  uint32_t fb_h = bi->fb_height;
  uint32_t fb_bpp = bi->fb_bpp;
  uint32_t fb_bytes = fb_pitch * fb_h;

  // Map framebuffer using 2MB huge pages in the device mapping area
  uint64_t fb_2mb_start = fb_phys & ~0x1FFFFFULL;
  uint64_t fb_2mb_end = ALIGN_UP(fb_phys + fb_bytes, 0x200000);
  size_t num_fb_2mb = (fb_2mb_end - fb_2mb_start) / 0x200000;

  // Find first free PDPT_hh entry (after RAM mappings)
  int pdpt_start = 510;
  for (int i = pdpt_start; i < 512; i++) {
    if (pdpt_hh[i] == 0) {
      pdpt_start = i;
      break;
    }
  }

  // Allocate a PD for the framebuffer
  uint64_t *fb_pd = (uint64_t *)bump_alloc(4096);
  uintptr_t fb_pd_phys = PHY_ADDR((uintptr_t)fb_pd);

  // Clear PD
  for (int i = 0; i < 512; i++) {
    fb_pd[i] = 0;
  }

  // Fill PD with 2MB huge pages mapping the framebuffer physical region
  for (size_t n = 0; n < num_fb_2mb; n++) {
    uint64_t page_phys = fb_2mb_start + (uint64_t)n * 0x200000;
    fb_pd[n] = page_phys | 0x83;  // Present + RW + PS (2MB huge page)
  }

  // Install PD into PDPT_hh
  pdpt_hh[pdpt_start] = fb_pd_phys | 0x03;

  // 虚拟地址: PML4[511] 的基址 + PDPT_hh[pdpt_start] 的偏移
  // PML4[511] 对应 0xFFFFFF0000000000 区域 (sign-extended 9-bit index)
  // PDPT_hh[510] = 0xFFFFFFFFC0000000, PDPT_hh[511] = 0xFFFFFFFF00000000 (wraps)
  // 通用公式: device_vma_base 已经由 extend_mapping 计算好
  uint64_t fb_vma_base = device_vma_base;
  device_vma_base += (uint64_t)num_fb_2mb * 0x200000;

  flush_tlb();

  fb.width = fb_w;
  fb.height = fb_h;
  fb.pitch = fb_pitch;
  fb.bpp = fb_bpp;
  fb.vaddr = (void *)(fb_vma_base + (fb_phys - fb_2mb_start));
  fb.size = fb_bytes;
  fb.phys_addr = fb_phys;

  cursor.x = 0;
  cursor.y = 0;
}

void clear() {
  IrqGuard guard;
  uint8_t *dst = (uint8_t *)fb.vaddr;
  for (size_t i = 0; i < fb.size; i++) {
    dst[i] = 0;
  }
  cursor.x = 0;
  cursor.y = 0;
}

static void scroll_up() {
  uint32_t line_bytes = fb.pitch * FONT_HEIGHT;
  uint8_t *fb_buf = (uint8_t *)fb.vaddr;
  for (uint32_t i = 0; i < fb.size - line_bytes; i++) {
    fb_buf[i] = fb_buf[i + line_bytes];
  }
  for (uint32_t i = fb.size - line_bytes; i < fb.size; i++) {
    fb_buf[i] = 0;
  }
}

static void advance_line() {
  if (cursor.y < (int32_t)rows() - 1) {
    cursor.y++;
  } else {
    scroll_up();
  }
}

void fb_putc(char c, uint32_t fg) {
  if (fb.vaddr == NULL) {
    return;
  }

  if (c == '\n') {
    cursor.x = 0;
    advance_line();
    return;
  }

  if (c == '\r') {
    cursor.x = 0;
    return;
  }

  if (c == '\t') {
    uint32_t next_tab = ALIGN_UP(cursor.x + 1, 8);
    if (next_tab >= cols()) {
      cursor.x = 0;
      advance_line();
    } else {
      cursor.x = next_tab;
    }
    return;
  }

  if (c < FONT_CHARS_START || c > 0x7E) {
    return;
  }

  const uint8_t *glyph = font8x16[c - FONT_CHARS_START];
  uint32_t px = cursor.x * FONT_WIDTH;
  uint32_t py = cursor.y * FONT_HEIGHT;
  uint32_t bpp_bytes = fb.bpp / 8;

  for (int row = 0; row < FONT_HEIGHT; row++) {
    uint8_t bits = glyph[row];
    uint8_t *dst = (uint8_t *)fb.vaddr + (py + row) * fb.pitch + px * bpp_bytes;
    for (int col = 0; col < FONT_WIDTH; col++) {
      if (bits & (0x80 >> col)) {
        if (fb.bpp == 32) {
          *(uint32_t *)dst = fg;
        } else {
          dst[0] = fg & 0xFF;
          dst[1] = (fg >> 8) & 0xFF;
          dst[2] = (fg >> 16) & 0xFF;
        }
      }
      dst += bpp_bytes;
    }
  }

  cursor.x++;
  if (cursor.x >= cols()) {
    cursor.x = 0;
    advance_line();
  }
}

void prints(const char *s, uint32_t fg) {
  while (*s) {
    fb_putc(*s++, fg);
  }
}

uint32_t cursor_get_x() { return cursor.x; }
uint32_t cursor_get_y() { return cursor.y; }
uint32_t cursor_get_width() { return fb.width; }
uint32_t cursor_get_height() { return fb.height; }

void cursor_move(uint32_t x, uint32_t y) {
  cursor.x = x;
  cursor.y = y;
}
