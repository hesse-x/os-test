// ===================== Struct 定义 =====================

struct Framebuffer {
  uint32_t width;      // 像素宽度
  uint32_t height;     // 像素高度
  uint32_t pitch;      // 每行字节数
  uint32_t bpp;        // 每像素位数
  void *vaddr;         // 映射后的虚拟地址
  size_t size;         // 显存总大小 (pitch * height)
  uint64_t phys_addr;  // 物理地址
};

struct Cursor {
  uint32_t x;          // 光标列（字符单位）
  uint32_t y;          // 光标行（字符单位）
};

